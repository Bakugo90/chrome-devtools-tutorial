// This JavaScript file provides functionality for the Sources Tab example.
// It demonstrates how to inspect scripts and resources using Chrome Developer Tools.

// Function to log a message to the console
function logMessage() {
    console.log("This is a message from the Sources Tab example.");
}

// Function to demonstrate inspecting a variable
function inspectVariable() {
    const exampleVariable = "Inspect me!";
    console.log(exampleVariable); // Inspect this variable in the console
}

// Function to simulate a simple operation
function simpleOperation() {
    const result = 5 + 10;
    console.log("The result of the operation is:", result); // Log the result
}

// Call the functions to demonstrate their functionality
logMessage();
inspectVariable();
simpleOperation();